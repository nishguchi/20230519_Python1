# Python

#for文 (リストループ)

a = [1, 2, 3]

print("【for文 (リストループ)】")
for n in a:		# 配列分ループ
	print(n)	# 配列要素が入る

print("")

b = ["red", "green","blue"]
for n in b:
	print(n)

print("")

print("【for文 (範囲ループ1)】")
for n in range(3):	# 表示 0～2 3回ループ
	print(n)

print("")

print("【for文 (範囲ループ2)】")
for n in range(5,8):	# 表示 4～7 3回ループ
	print(n)

print("")

print("【for文 (範囲ループ3_1)】")
for n in range(0,50,5):	# 表示 0～50まで 5回に1回 表示ループ
	print(n)

print("")


print("【for文 (範囲ループ3_2)】")
for n in range(1,50,5):	# 表示 1～50まで 5回に1回 表示ループ
	print(n)

print("")


print("【for文 (範囲ループ3_3)】")
for n in range(0,50,5):	# 表示 0～50まで 5回に1回 表示ループ
	print(n+1)			# 表示は n + 1

print("")



print("【for文 (範囲ループ3_4)】")
for n in range(0,50,5):	# 表示 0～50まで 5回に1回 表示ループ
	print(n-5)			# 表示は n - 5

print("")









